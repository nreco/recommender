		/**
		 * Internal helper method for natural logarithm function.
		 * @param x original argument of the natural logarithm function
		 * @param hiPrec extra bits of precision on output (To Be Confirmed)
		 * @return log(x)
		 */
		private static double Log(double x, double[] hiPrec) {
			if (x == 0) { // Handle special case of +0/-0
				return Double.NegativeInfinity;
			}
			long bits = BitConverter.DoubleToInt64Bits(x);

			/* Handle special cases of negative input, and NaN */
			if ((((uint)bits & 0x8000000000000000L) != 0 || x != x) && x != 0.0) {
				if (hiPrec != null) {
					hiPrec[0] = Double.NaN;
				}

				return Double.NaN;
			}

			/* Handle special cases of Positive infinity. */
			if (x == Double.PositiveInfinity) {
				if (hiPrec != null) {
					hiPrec[0] = Double.PositiveInfinity;
				}

				return Double.PositiveInfinity;
			}

			/* Extract the exponent */
			int exp = (int)(bits >> 52) - 1023;

			if ((bits & 0x7ff0000000000000L) == 0) {
				// Subnormal!
				if (x == 0) {
					// Zero
					if (hiPrec != null) {
						hiPrec[0] = Double.NegativeInfinity;
					}

					return Double.NegativeInfinity;
				}

				/* Normalize the subnormal number. */
				bits <<= 1;
				while ((bits & 0x0010000000000000L) == 0) {
					--exp;
					bits <<= 1;
				}
			}


			if ((exp == -1 || exp == 0) && x < 1.01 && x > 0.99 && hiPrec == null) {
				/* The normal method doesn't work well in the range [0.99, 1.01], so call do a straight
			   polynomial expansion in higer precision. */

				/* Compute x - 1.0 and split it */
				double xa = x - 1.0;
				double xb = xa - x + 1.0;
				double tmp = xa * HEX_40000000;
				double aa = xa + tmp - tmp;
				double ab = xa - aa;
				xa = aa;
				xb = ab;

				var ln_quick_coef_idx = LN_QUICK_COEF.GetLength(0) - 1;
				double ya = LN_QUICK_COEF[ln_quick_coef_idx, 0];
				double yb = LN_QUICK_COEF[ln_quick_coef_idx, 1];

				for (int i = LN_QUICK_COEF.GetLength(0) - 2; i >= 0; i--) {
					/* Multiply a = y * x */
					aa = ya * xa;
					ab = ya * xb + yb * xa + yb * xb;
					/* split, so now y = a */
					tmp = aa * HEX_40000000;
					ya = aa + tmp - tmp;
					yb = aa - ya + ab;

					/* Add  a = y + lnQuickCoef */
					aa = ya + LN_QUICK_COEF[i, 0];
					ab = yb + LN_QUICK_COEF[i, 1];
					/* Split y = a */
					tmp = aa * HEX_40000000;
					ya = aa + tmp - tmp;
					yb = aa - ya + ab;
				}

				/* Multiply a = y * x */
				aa = ya * xa;
				ab = ya * xb + yb * xa + yb * xb;
				/* split, so now y = a */
				tmp = aa * HEX_40000000;
				ya = aa + tmp - tmp;
				yb = aa - ya + ab;

				return ya + yb;
			}

			// lnm is a log of a number in the range of 1.0 - 2.0, so 0 <= lnm < ln(2)
			var lnmIdx = (int)((bits & 0x000ffc0000000000L) >> 42);
			var lnm0 = lnMant.LN_MANT[lnmIdx, 0];
			var lnm1 = lnMant.LN_MANT[lnmIdx, 1];

			/*
		double epsilon = x / Double.longBitsToDouble(bits & 0xfffffc0000000000L);

		epsilon -= 1.0;
			 */

			// y is the most significant 10 bits of the mantissa
			//double y = Double.longBitsToDouble(bits & 0xfffffc0000000000L);
			//double epsilon = (x - y) / y;
			double epsilon = (bits & 0x3ffffffffffL) / (TWO_POWER_52 + (bits & 0x000ffc0000000000L));

			double lnza = 0.0;
			double lnzb = 0.0;

			if (hiPrec != null) {
				/* split epsilon -> x */
				double tmp = epsilon * HEX_40000000;
				double aa = epsilon + tmp - tmp;
				double ab = epsilon - aa;
				double xa = aa;
				double xb = ab;

				/* Need a more accurate epsilon, so adjust the division. */
				double numer = bits & 0x3ffffffffffL;
				double denom = TWO_POWER_52 + (bits & 0x000ffc0000000000L);
				aa = numer - xa * denom - xb * denom;
				xb += aa / denom;

				/* Remez polynomial evaluation */
				var ln_hi_prec_coef_last_idx = LN_HI_PREC_COEF.GetLength(0) - 1;
				double ya = LN_HI_PREC_COEF[ln_hi_prec_coef_last_idx, 0];
				double yb = LN_HI_PREC_COEF[ln_hi_prec_coef_last_idx, 1];

				for (int i = LN_HI_PREC_COEF.GetLength(0) - 2; i >= 0; i--) {
					/* Multiply a = y * x */
					aa = ya * xa;
					ab = ya * xb + yb * xa + yb * xb;
					/* split, so now y = a */
					tmp = aa * HEX_40000000;
					ya = aa + tmp - tmp;
					yb = aa - ya + ab;

					/* Add  a = y + lnHiPrecCoef */
					aa = ya + LN_HI_PREC_COEF[i, 0];
					ab = yb + LN_HI_PREC_COEF[i, 1];
					/* Split y = a */
					tmp = aa * HEX_40000000;
					ya = aa + tmp - tmp;
					yb = aa - ya + ab;
				}

				/* Multiply a = y * x */
				aa = ya * xa;
				ab = ya * xb + yb * xa + yb * xb;

				/* split, so now lnz = a */
				/*
		  tmp = aa * 1073741824.0;
		  lnza = aa + tmp - tmp;
		  lnzb = aa - lnza + ab;
				 */
				lnza = aa + ab;
				lnzb = -(lnza - aa - ab);
			} else {
				/* High precision not required.  Eval Remez polynomial
			 using standard double precision */
				lnza = -0.16624882440418567;
				lnza = lnza * epsilon + 0.19999954120254515;
				lnza = lnza * epsilon + -0.2499999997677497;
				lnza = lnza * epsilon + 0.3333333333332802;
				lnza = lnza * epsilon + -0.5;
				lnza = lnza * epsilon + 1.0;
				lnza *= epsilon;
			}

			/* Relative sizes:
			 * lnzb     [0, 2.33E-10]
			 * lnm[1]   [0, 1.17E-7]
			 * ln2B*exp [0, 1.12E-4]
			 * lnza      [0, 9.7E-4]
			 * lnm[0]   [0, 0.692]
			 * ln2A*exp [0, 709]
			 */

			/* Compute the following sum:
			 * lnzb + lnm[1] + ln2B*exp + lnza + lnm[0] + ln2A*exp;
			 */

			//return lnzb + lnm[1] + ln2B*exp + lnza + lnm[0] + ln2A*exp;
			double a = LN_2_A * exp;
			double b = 0.0;
			double c = a + lnm0;
			double d = -(c - a - lnm0);
			a = c;
			b += d;

			c = a + lnza;
			d = -(c - a - lnza);
			a = c;
			b += d;

			c = a + LN_2_B * exp;
			d = -(c - a - LN_2_B * exp);
			a = c;
			b += d;

			c = a + lnm1;
			d = -(c - a - lnm1);
			a = c;
			b += d;

			c = a + lnzb;
			d = -(c - a - lnzb);
			a = c;
			b += d;

			if (hiPrec != null) {
				hiPrec[0] = a;
				hiPrec[1] = b;
			}

			return a + b;
		}

		private static double F_1_3 = 1d / 3d;
		private static double F_1_2 = 1d / 2d;

		public static double Log1p(double x) {
			if (x == -1) {
				return Double.NegativeInfinity;
			}

			if (x == Double.PositiveInfinity) {
				return Double.PositiveInfinity;
			}

			if (x > 1e-6 ||
				x < -1e-6) {
				double xpa = 1 + x;
				double xpb = -(xpa - 1 - x);

				double[] hiPrec = new double[2];
				double lores = Log(xpa, hiPrec);
				if (Double.IsInfinity(lores)) { // Don't allow this to be converted to NaN
					return lores;
				}

				// Do a taylor series expansion around xpa:
				//   f(x+y) = f(x) + f'(x) y + f''(x)/2 y^2
				double fx1 = xpb / xpa;
				double epsilon = 0.5 * fx1 + 1;
				return epsilon * fx1 + hiPrec[1] + hiPrec[0];
			} else {
				// Value is small |x| < 1e6, do a Taylor series centered on 1.
				double y = (x * F_1_3 - F_1_2) * x + 1;
				return y * x;
			}
		}


